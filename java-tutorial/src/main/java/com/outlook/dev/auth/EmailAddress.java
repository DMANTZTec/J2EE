package com.outlook.dev.auth;

public class EmailAddress {
	private String Address;
	private String Name;

	public String getAddress() {
		return Address;
	}

	public void setAddress(String address) {
		Address = address;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

}


