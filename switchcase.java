class switchcase {
	public static void main(String args[]) {
		int x = 3;
		switch(x) {
		
		case 0:
			System.out.println("x=1");
			break;
			
		case 1:
			System.out.println("x=2");
			break;
			
		case 3:
			System.out.println("x=3");
			break;
			
		case 4:
			System.out.println("x=4");
			break;
		}
		
	}
}
